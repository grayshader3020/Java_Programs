class NarrowingLong1{
	public static void main(String[] args) {
		long b = 1034794598693l;
		System.out.println(b);

		int c = (int)b;
		System.out.println(c);

		char e = (char)b;
		System.out.println(e);

		short g = (short)b;
		System.out.println(g);

		byte k = (byte) b;
		System.out.println(k);
	}
}